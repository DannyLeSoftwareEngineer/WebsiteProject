                <p>Burnie couldn't shake the thought-provoking conversation. It haunted him. Why WAS it called a Warthog? Not a single other person in the entire world cared (and still doesn't), but he had to know. So he decided to create his own answer, and Red vs. Blue was born. He frantically called Geoff and Gus to tell them about his new idea, a movie INSIDE of a video game. They mocked him (and continue to, to this day). The world wasn't ready for his idea. No one could grasp the concept. Undaunted, he "filmed" a trailer to show them. They still didn't care. Burnie was heartbroken. Soon after, in a completely unrelated incident, Drunkgamers.com ended. No one knows exactly why or what caused the site's demise, but some sources say a middle finger (possibly two), combined with six Irish Car Bombs, were partially to blame. This looked like the end.</p>
                <p>Until...</p>
                <p>One day, four months later, Computer Gaming World contacted Geoff to ask if they could use an old Drunkgamers video on one of their cover mount CDs. Geoff graciously accepted, and he and Burnie lamented about the pointlessness of having a video in a magazine of such stature, with no website to point to it. The group reunited in a flurry of activity. Plans were made, schemes were hatched, and just as they were settling on the idea of a Justin Timberlake fan site, Burnie remembered his little Halo movie. He knew there was only one way to convince Gus and Geoff (who were already scanning in their Timberlake pics), so he got them stinking drunk. They awoke the next day, hung over and strangely sore (but that's a story for another day), to see that Burnie had convinced them to sign their lives away for the promise of more banana daiquiris. They were beaten. He immediately called Matt and Joel to gloat, only to discover that they too were beaten. Broken down by the harsh and unforgiving Tinseltown machine. They begged him for a job, even if it was just washing cars. Burnie decided to be the bigger man, and fences were mended (literally, he made them build a fence in his back yard).</p>
