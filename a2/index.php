                <p>To completely tell the story, I'll need to go back a few years.</p>
                <p>The mid 90s were a confusing time for our nation. Being politically correct was en vogue, Madonna was just beginning to show the first signs of old age, and three sub-standard students met on the campus of the University of Texas At Austin. Realizing that their grades would no doubt keep them out of any of the higher paying jobs the world had to offer, Burnie Burns, Matt Hullum, and Joel Heyman began to explore other ideas. A seed was planted, and an independent movie was born. Burnie and Matt (being the ugliest of the three) wrote and directed the film, while the much more aesthetically pleasing Joel starred in it. It was called The Schedule and centered around a regular Joe who got stuck with the unfortunate duty of collecting souls for Death, the lowest-paying employer of them all. They trio toured the film festival scene, and encountered some success. Unfortunately, as their friendship grew, so did other feelings. As love triangles usually do, their relationship ended in shambles. Joel, unable to handle the complicated feelings and emotions, fled to Los Angeles to further pursue a career in acting, the other lowest-paying job of them all. Matt was torn, but decided to follow his heart (i.e., Joel), and ended up in LA as well. Still too ugly for acting, he began a career in Hollywood doing visual effects. He also began the long process of healing and growing said relationship with Joel.</p>
                <p>Burnie was crushed. To deal with this double heartbreak, he became emotionally vapid, and did the only thing available for the apathetic and soulless: he entered the tech industry. His entropy helped propel him to the top, and eventually he became the Director of Operations at a tech support center, where a new group of poor students were just beginning to emerge.Gustavo Sorola, fleeing from his stunning failures at Rice University, met Geoff Fink, a man who was simply too dumb for college. They embraced their low-paying tech jobs, falling in love with the primary colors and blinking text of the internet. A kinship was formed, and the pair became fast friends, often playing video games till all hours of the morning while sharing their feelings and desires. Pillow fights were had, toenails were painted, and much alcohol consumed. It was heaven on earth.  </p>
                <p>The two quickly became disillusioned with the internet and learned to hate its mocking ways, so they created a website in defense. It was called Uglyinternet.com and it was mean. Too mean for the world. Lawsuits were filed, and death threats issued. The pair bought a baseball bat and hid in a closet for six months. When they emerged, they had a dream to start another website, one that combined god's two greatest gifts to mankind: drinking and video games. </p>
                <p>Drunkgamers.com began soon after in the spring of 2000. The site began to grow a small following, and much like the moth is drawn to the flame, Burnie was drawn to their radical ideas and raw passion. Slowly, Geoff and Gus taught Burnie how to care again. He began to write for them, and soon became the most prolific drunken video game writer of all time. Trying to find a way to weasel his quasi-filmmaking experience into the site, Burnie started adding weekly gameplay videos. No one watched them, but he refused to stop. One day while making a video showing how great he thought he was at Halo, he remembered a recent conversation during a game of CTF. It might have gone something like this.</p>
